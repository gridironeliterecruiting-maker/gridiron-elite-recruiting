import type { NextConfig } from "next";
import { writeFileSync, readFileSync } from "fs";
import { join } from "path";

// Capture Twitter env vars at build time.
// Vercel injects env vars at build time but NOT into serverless function runtime.
// Strategy 1: Generate a TS module with hardcoded values
// Strategy 2: Use next.config env property to inline process.env replacements
const twitterClientId = process.env.TWITTER_CLIENT_ID || "";
const twitterClientSecret = process.env.TWITTER_CLIENT_SECRET || "";
const buildTime = new Date().toISOString();

// Trim values (Vercel env vars may have trailing newlines)
const trimmedId = twitterClientId.trim();
const trimmedSecret = twitterClientSecret.trim();

const genPath = join(process.cwd(), "src/lib/_twitter-build-config.ts");
writeFileSync(
  genPath,
  `// Auto-generated by next.config.ts at build time — do not edit\n` +
    `export const TEST_CONSTANT = "hello_test_abc123";\n` +
    `export const TWITTER_CLIENT_ID = ${JSON.stringify(trimmedId)};\n` +
    `export const TWITTER_CLIENT_SECRET = ${JSON.stringify(trimmedSecret)};\n` +
    `export const BUILD_TIMESTAMP = ${JSON.stringify(buildTime)};\n`
);

const written = readFileSync(genPath, "utf8");
console.log(`[next.config.ts] cwd=${process.cwd()}`);
console.log(`[next.config.ts] ID=${trimmedId.length}, Secret=${trimmedSecret.length}`);
console.log(`[next.config.ts] Generated file:\n${written}`);

const nextConfig: NextConfig = {
  env: {
    BAKED_TWITTER_CLIENT_ID: trimmedId,
    BAKED_TWITTER_CLIENT_SECRET: trimmedSecret,
    BAKED_TWITTER_BUILD_TIMESTAMP: buildTime,
    TEST_ENV_CONSTANT: "env_test_xyz789",
  },
};

export default nextConfig;
