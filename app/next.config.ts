import type { NextConfig } from "next";
import { writeFileSync } from "fs";
import { join } from "path";

// Vercel's post-build processing strips any string literal matching an encrypted
// env var value from compiled output. To bypass this, we store values as arrays
// of character codes (numbers) and reconstruct strings at runtime.
const id = (process.env.TWITTER_CLIENT_ID || "").trim();
const secret = (process.env.TWITTER_CLIENT_SECRET || "").trim();
const ts = new Date().toISOString();

const toCharCodes = (s: string) => JSON.stringify(Array.from(s).map(c => c.charCodeAt(0)));

writeFileSync(
  join(process.cwd(), "src/lib/_twitter-build-config.ts"),
  [
    "// Auto-generated by next.config.ts — do not edit",
    "const d = (a: number[]) => a.map(c => String.fromCharCode(c)).join('');",
    `export const TWITTER_CLIENT_ID = d(${toCharCodes(id)});`,
    `export const TWITTER_CLIENT_SECRET = d(${toCharCodes(secret)});`,
    `export const BUILD_TIMESTAMP = ${JSON.stringify(ts)};`,
    "",
  ].join("\n")
);

console.log(`[next.config.ts] charcode gen: ID=${id.length}, Secret=${secret.length}`);

const nextConfig: NextConfig = {};

export default nextConfig;
